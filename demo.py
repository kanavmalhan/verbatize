def evaluate_network(files, args):
	# GPU: active speaker detection by pretrained TalkNet, batched for high VRAM utilization
	s = talkNet()
	s.loadParameters(args.pretrainModel)
	sys.stderr.write("Model %s loaded from previous state! \r\n"%args.pretrainModel)
	s.eval()

	durationSet = {1,1,1,2,2,2,3,3,4,5,6}

	# ── 1. Load all clip features upfront ─────────────────────────────────
	sys.stderr.write("Loading all clip features...\r\n")
	all_audio_features, all_video_features, file_lengths = [], [], []

	for file in tqdm.tqdm(files, total=len(files), desc="Loading clips"):
		fileName  = os.path.splitext(os.path.basename(file))[0]
		audioFile = os.path.join(os.path.dirname(file), fileName + '.wav')
		_, audio  = wavfile.read(audioFile)
		audioFeature = python_speech_features.mfcc(audio, 16000, numcep=13, winlen=0.025, winstep=0.010)

		video = cv2.VideoCapture(file)
		videoFeature = []
		while video.isOpened():
			ret, frames = video.read()
			if ret == True:
				face = cv2.cvtColor(frames, cv2.COLOR_BGR2GRAY)
				face = cv2.resize(face, (224,224))
				face = face[int(112-(112/2)):int(112+(112/2)), int(112-(112/2)):int(112+(112/2))]
				videoFeature.append(face)
			else:
				break
		video.release()
		videoFeature = numpy.array(videoFeature)

		length = min((audioFeature.shape[0] - audioFeature.shape[0] % 4) / 100, videoFeature.shape[0] / 25)
		audioFeature = audioFeature[:int(round(length * 100)),:]
		videoFeature = videoFeature[:int(round(length * 25)),:,:]

		all_audio_features.append(audioFeature)
		all_video_features.append(videoFeature)
		file_lengths.append(length)

	# ── 2. Build every duration window as a tensor job ────────────────────
	sys.stderr.write("Building inference jobs...\r\n")
	# Each job: (file_idx, duration, window_idx, inputA, inputV)
	jobs = []
	for f_idx, (audioFeature, videoFeature, length) in enumerate(zip(all_audio_features, all_video_features, file_lengths)):
		for duration in durationSet:
			n_windows = int(math.ceil(length / duration))
			for i in range(n_windows):
				inputA = torch.FloatTensor(audioFeature[i*duration*100:(i+1)*duration*100, :]).unsqueeze(0)
				inputV = torch.FloatTensor(videoFeature[i*duration*25:(i+1)*duration*25, :, :]).unsqueeze(0)
				jobs.append((f_idx, duration, i, inputA, inputV))

	# ── 3. Run inference in large GPU batches ─────────────────────────────
	sys.stderr.write("Running %d windows in batches of %d on GPU...\r\n" % (len(jobs), args.batchSize))
	results = {}  # keyed by (f_idx, duration, window_idx)

	with torch.no_grad():
		for batch_start in tqdm.tqdm(range(0, len(jobs), args.batchSize), desc="Inference"):
			batch = jobs[batch_start : batch_start + args.batchSize]

			# Pad to same temporal length within the batch so we can stack
			max_a = max(j[3].shape[1] for j in batch)
			max_v = max(j[4].shape[1] for j in batch)

			padded_a, padded_v = [], []
			for _, _, _, inputA, inputV in batch:
				if inputA.shape[1] < max_a:
					inputA = torch.nn.functional.pad(inputA, (0, 0, 0, max_a - inputA.shape[1]))
				if inputV.shape[1] < max_v:
					inputV = torch.nn.functional.pad(inputV, (0, 0, 0, 0, 0, max_v - inputV.shape[1]))
				padded_a.append(inputA)
				padded_v.append(inputV)

			batchA = torch.cat(padded_a, dim=0).cuda()  # (B, T_a, 13)
			batchV = torch.cat(padded_v, dim=0).cuda()  # (B, T_v, 224, 224)

			embedA = s.model.forward_audio_frontend(batchA)
			embedV = s.model.forward_visual_frontend(batchV)
			embedA, embedV = s.model.forward_cross_attention(embedA, embedV)
			out    = s.model.forward_audio_visual_backend(embedA, embedV)
			scores = s.lossAV.forward(out, labels=None)  # returns (B,) array

			for idx, (f_idx, duration, win_i, _, _) in enumerate(batch):
				results[(f_idx, duration, win_i)] = float(scores[idx])

	# ── 4. Reconstruct per-file averaged score arrays ─────────────────────
	sys.stderr.write("Reconstructing score arrays...\r\n")
	allScores = []
	for f_idx, length in enumerate(file_lengths):
		duration_scores = []
		for duration in durationSet:
			n_windows = int(math.ceil(length / duration))
			s_list = [results[(f_idx, duration, i)] for i in range(n_windows)]
			duration_scores.append(s_list)
		max_len = max(len(s) for s in duration_scores)
		padded  = [s + [s[-1]] * (max_len - len(s)) for s in duration_scores]
		allScore = numpy.round(numpy.mean(numpy.array(padded), axis=0), 1).astype(float)
		allScores.append(allScore)

	return allScores

def visualization(tracks, scores, args):
	# CPU: visulize the result for video format
	flist = glob.glob(os.path.join(args.pyframesPath, '*.jpg'))
	flist.sort()
	faces = [[] for i in range(len(flist))]
	for tidx, track in enumerate(tracks):
		score = scores[tidx]
		for fidx, frame in enumerate(track['track']['frame'].tolist()):
			s = score[max(fidx - 2, 0): min(fidx + 3, len(score) - 1)]
			s = numpy.mean(s)
			faces[frame].append({'track':tidx, 'score':float(s),'s':track['proc_track']['s'][fidx], 'x':track['proc_track']['x'][fidx], 'y':track['proc_track']['y'][fidx]})
	firstImage = cv2.imread(flist[0])
	fw = firstImage.shape[1]
	fh = firstImage.shape[0]
	vOut = cv2.VideoWriter(os.path.join(args.pyaviPath, 'video_only.avi'), cv2.VideoWriter_fourcc(*'XVID'), 25, (fw,fh))
	colorDict = {0: 0, 1: 255}
	for fidx, fname in tqdm.tqdm(enumerate(flist), total = len(flist)):
		image = cv2.imread(fname)
		for face in faces[fidx]:
			clr = colorDict[int((face['score'] >= 0))]
			txt = round(face['score'], 1)
			cv2.rectangle(image, (int(face['x']-face['s']), int(face['y']-face['s'])), (int(face['x']+face['s']), int(face['y']+face['s'])),(0,clr,255-clr),10)
			cv2.putText(image,'%s'%(txt), (int(face['x']-face['s']), int(face['y']-face['s'])), cv2.FONT_HERSHEY_SIMPLEX, 1.5, (0,clr,255-clr),5)
		vOut.write(image)
	vOut.release()
	command = ("ffmpeg -y -i %s -i %s -threads %d -c:v copy -c:a copy %s -loglevel panic" % \
		(os.path.join(args.pyaviPath, 'video_only.avi'), os.path.join(args.pyaviPath, 'audio.wav'), \
		args.nDataLoaderThread, os.path.join(args.pyaviPath,'video_out.avi')))
	output = subprocess.call(command, shell=True, stdout=None)

